/** Query defines the gRPC querier service. */
export interface Query {
  /** Parameters queries the parameters of the module. */
  Params(request: QueryParamsRequest): Promise<QueryParamsResponse>;
  /** Queries a list of GetCoin items. */
  GetCoin(request: QueryGetCoinRequest): Promise<QueryGetCoinResponse>;
  /** Queries a list of GetInfo items. */
  GetInfo(request: QueryGetInfoRequest): Promise<QueryGetInfoResponse>;
}

export class QueryClientImpl implements Query {
  private readonly rpc: Rpc;
  constructor(rpc: Rpc) {
    this.rpc = rpc;
  }
  Params(request: QueryParamsRequest): Promise<QueryParamsResponse> {
    const data = QueryParamsRequest.encode(request).finish();
    const promise = this.rpc.request("sujine.test.test.Query", "Params", data);
    return promise.then((data) => QueryParamsResponse.decode(new Reader(data)));
  }

  GetCoin(request: QueryGetCoinRequest): Promise<QueryGetCoinResponse> {
    const data = QueryGetCoinRequest.encode(request).finish();
    const promise = this.rpc.request("sujine.test.test.Query", "GetCoin", data);
    return promise.then((data) =>
      QueryGetCoinResponse.decode(new Reader(data))
    );
  }

  GetInfo(request: QueryGetInfoRequest): Promise<QueryGetInfoResponse> {
    const data = QueryGetInfoRequest.encode(request).finish();
    const promise = this.rpc.request("sujine.test.test.Query", "GetInfo", data);
    return promise.then((data) =>
      QueryGetInfoResponse.decode(new Reader(data))
    );
  }
}

interface Rpc {
  request(
    service: string,
    method: string,
    data: Uint8Array
  ): Promise<Uint8Array>;
}

type Builtin = Date | Function | Uint8Array | string | number | undefined;
export type DeepPartial<T> = T extends Builtin
  ? T
  : T extends Array<infer U>
  ? Array<DeepPartial<U>>
  : T extends ReadonlyArray<infer U>
  ? ReadonlyArray<DeepPartial<U>>
  : T extends {}
  ? { [K in keyof T]?: DeepPartial<T[K]> }
  : Partial<T>;
